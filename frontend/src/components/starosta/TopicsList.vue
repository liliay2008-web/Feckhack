        showAssignmentDialog.value = false;
        loadTopics();
      } else {
        const minutes = Math.floor(diff / 60000);
        const seconds = Math.floor((diff % 60000) / 1000);
        timeLeft.value = `${minutes}:${seconds.toString().padStart(2, '0')}`;
      }
    }
  }, 1000);
};
